let easyBibleVerseCache = {};

function parseEasyBibleVersesForChapter(rawText, chapterNumber) {
    const verseMap = {};
    const lines = rawText.split(/\r?\n/);
    let currentVerse = null;

    lines.forEach(line => {
        const trimmed = line.trim();
        if (!trimmed) return;

        const verseMatch = trimmed.match(/^(\d+)\s*:\s*(\d+)\s*(.*)$/);
        if (verseMatch) {
            const chapter = Number(verseMatch[1]);
            const verse = Number(verseMatch[2]);
            const verseText = (verseMatch[3] || '').trim();

            if (chapter !== chapterNumber || !Number.isFinite(verse)) {
                currentVerse = null;
                return;
            }

            verseMap[verse] = verseText;
            currentVerse = verse;
            return;
        }

        if (currentVerse !== null) {
            verseMap[currentVerse] = `${verseMap[currentVerse]} ${trimmed}`.trim();
        }
    });

    return verseMap;
}

function getEasyBibleVerseMap(chapterNumber) {
    const chapterKey = String(chapterNumber);
    if (Object.prototype.hasOwnProperty.call(easyBibleVerseCache, chapterKey)) {
        return easyBibleVerseCache[chapterKey];
    }

    const rawText = easyBibleTextByChapter[chapterKey] || '';
    const parsed = parseEasyBibleVersesForChapter(rawText, chapterNumber);
    easyBibleVerseCache[chapterKey] = parsed;
    return parsed;
}

function parseVerseRangeFromTitle(sectionTitle) {
    const match = sectionTitle.match(/\((\d+)\s*:\s*(\d+)\s*[~\-–]\s*(\d+)\)/);
    if (!match) return null;

    const chapter = Number(match[1]);
    const startVerse = Number(match[2]);
    const endVerse = Number(match[3]);

    if (!Number.isFinite(chapter) || !Number.isFinite(startVerse) || !Number.isFinite(endVerse)) {
        return null;
    }

    return {
        chapter,
        startVerse: Math.min(startVerse, endVerse),
        endVerse: Math.max(startVerse, endVerse)
    };
}

function getEasyBibleRangeLines(rangeInfo) {
    const verseMap = getEasyBibleVerseMap(rangeInfo.chapter);
    const lines = [];

    for (let verse = rangeInfo.startVerse; verse <= rangeInfo.endVerse; verse++) {
        if (verseMap[verse]) {
            lines.push(`${rangeInfo.chapter}:${verse} ${verseMap[verse]}`);
        }
    }

    return lines;
}

