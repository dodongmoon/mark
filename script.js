/** 
 * 카드 1장을 표시하는 함수:
 * - 문제만 즉시 표시
 * - (currentFlashcardSpeed)ms 후 정답을 표시
 */
function showNextFlashcard() {
    if (currentQuizData.length === 0) {
        flashcardQuestionEl.textContent = '데이터 없음';
        flashcardAnswerEl.textContent = '';
        stopFlashcards();
        return;
    }

    // 인덱스 범위 체크
    if (flashcardIndex >= currentQuizData.length) {
        flashcardIndex = 0;
    }

    const card = currentQuizData[flashcardIndex];

    // 1) 문제 표시 (정답 숨김)
    flashcardQuestionEl.textContent = `${flashcardIndex + 1}. ${card.q}`;
    flashcardAnswerEl.textContent = '';

    // 2) (currentFlashcardSpeed) 밀리초 뒤에 정답을 잠깐(0.3초) 보여줌
    setTimeout(() => {
        flashcardAnswerEl.textContent = `답: ${card.a}`;
    }, currentFlashcardSpeed);

    flashcardIndex++;
}

/** 
 * startFlashcards():
 * - 첫 카드 보여주고
 * - (currentFlashcardSpeed + REVEAL_ANSWER_DELAY) 간격으로 반복
 */
function startFlashcards() {
    if (flashcardIntervalId || currentQuizData.length === 0) return;

    stopFlashcardsBtn.disabled = false;
    startFlashcardsBtn.disabled = true;
    restartFlashcardsBtn.disabled = false;

    // 첫 카드를 즉시 보여주고, 이후 인터벌로 반복
    showNextFlashcard();
    flashcardIntervalId = setInterval(showNextFlashcard, currentFlashcardSpeed + REVEAL_ANSWER_DELAY);
}

/** stopFlashcards(): Interval 해제 */
function stopFlashcards() {
    clearInterval(flashcardIntervalId);
    flashcardIntervalId = null;

    startFlashcardsBtn.disabled = (currentQuizData.length === 0);
    stopFlashcardsBtn.disabled = true;
    restartFlashcardsBtn.disabled = (currentQuizData.length === 0);
}

/** restartFlashcards(): 인덱스 0부터 재시작 */
function restartFlashcards() {
    stopFlashcards();
    flashcardIndex = 0;
    clearFlashcardDisplay();
    if (currentQuizData.length > 0) {
        startFlashcards();
    } else {
        flashcardQuestionEl.textContent = '데이터 없음';
        restartFlashcardsBtn.disabled = true;
    }
}

