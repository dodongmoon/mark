function renderAdultSummary(text) {
    const container = document.getElementById('summary-text-adult');
    container.innerHTML = '';

    const adultContainer = document.createElement('div');
    adultContainer.className = 'adult-summary-container';

    const sectionRegex = /(\d+\.\s+[^\n]+)([^]*?)(?=\d+\.\s+|\s*$)/g;
    let match, foundSection = false;

    while ((match = sectionRegex.exec(text)) !== null) {
        foundSection = true;
        const sectionTitle = match[1].trim();
        const sectionContent = match[2].trim();

        const sectionDiv = document.createElement('div');
        sectionDiv.className = 'adult-summary-section';

        const headerEl = document.createElement('h3');
        headerEl.textContent = sectionTitle.replace(/^\d+\.\s+/, '');
        const headerRow = document.createElement('div');
        headerRow.className = 'adult-summary-header-row';

        const numberMatch = sectionTitle.match(/^(\d+)\./);
        if (numberMatch) {
            const sectionNumber = numberMatch[1];
            const numberSpan = document.createElement('span');
            numberSpan.className = 'section-number';
            numberSpan.textContent = sectionNumber;
            headerEl.insertBefore(numberSpan, headerEl.firstChild);
        }
        headerRow.appendChild(headerEl);

        const contentEl = document.createElement('p');
        contentEl.textContent = sectionContent;

        const rangeInfo = parseVerseRangeFromTitle(sectionTitle);
        if (rangeInfo) {
            const toggleBtn = document.createElement('button');
            toggleBtn.type = 'button';
            toggleBtn.className = 'easy-bible-toggle-btn';
            toggleBtn.textContent = '쉬운말 성경 보기';

            const rangeBox = document.createElement('div');
            rangeBox.className = 'easy-bible-range-box';
            const rangeLines = getEasyBibleRangeLines(rangeInfo);
            rangeBox.textContent = rangeLines.length > 0
                ? rangeLines.join('\n')
                : `${rangeInfo.chapter}:${rangeInfo.startVerse}~${rangeInfo.endVerse} 원문이 없습니다. script.js의 easyBibleTextByChapter에 입력해 주세요.`;

            toggleBtn.addEventListener('click', () => {
                const willOpen = !rangeBox.classList.contains('visible');
                rangeBox.classList.toggle('visible', willOpen);
                toggleBtn.textContent = willOpen ? '쉬운말 성경 숨기기' : '쉬운말 성경 보기';
            });

            headerRow.appendChild(toggleBtn);
            sectionDiv.appendChild(headerRow);
            sectionDiv.appendChild(contentEl);
            sectionDiv.appendChild(rangeBox);
            adultContainer.appendChild(sectionDiv);
            continue;
        }

        sectionDiv.appendChild(headerRow);
        sectionDiv.appendChild(contentEl);
        adultContainer.appendChild(sectionDiv);
    }

    if (!foundSection) {
        const paragraphs = text.split(/\n\s*\n+/);
        paragraphs.forEach(p => {
            if (!p.trim()) return;
            const div = document.createElement('div');
            div.className = 'adult-summary-section';
            const contentEl = document.createElement('p');
            contentEl.textContent = p.trim();
            div.appendChild(contentEl);
            adultContainer.appendChild(div);
        });
    }

    container.appendChild(adultContainer);
}

